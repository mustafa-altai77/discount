package com.example.discaount.data.model;

public class UserProfile {
     int id;
    String name;
    String phone;
    String address;
    String age;
    String token_firebase;
    String otp_code;

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getPhone() {
        return phone;
    }

    public String getAddress() {
        return address;
    }

    public String getAge() {
        return age;
    }

    public String getToken_firebase() {
        return token_firebase;
    }

    public String getOtp_code() {
        return otp_code;
    }
}
